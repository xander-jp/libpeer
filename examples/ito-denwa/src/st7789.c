static void lcd_draw_char(int x, int y, char c, int scale, uint16_t fg, uint16_t bg) {
    if (scale < 1) scale = 1;
    if (scale > 4) scale = 4;
    const uint8_t* g = font5x7[(uint8_t)c & 0x7F];
    int w = 6 * scale;  // 5 cols + 1 col spacing
    int h = 8 * scale;
    if (x + w > LCD_W || y + h > LCD_H) return;

    lcd_set_window(x, y, x + w - 1, y + h - 1);

    uint8_t hi_fg = fg >> 8, lo_fg = fg & 0xFF;
    uint8_t hi_bg = bg >> 8, lo_bg = bg & 0xFF;
    uint8_t row_buf[6 * 4 * 2];  // max scale=4

    cs_low();
    gpio_put(LCD_PIN_DC, 1);
    for (int dy = 0; dy < 8; dy++) {
        int idx = 0;
        for (int col = 0; col < 6; col++) {
            uint8_t bits = (col < 5) ? g[col] : 0;
            bool on = (bits >> dy) & 1;
            for (int sx = 0; sx < scale; sx++) {
                if (on) { row_buf[idx++] = hi_fg; row_buf[idx++] = lo_fg; }
                else    { row_buf[idx++] = hi_bg; row_buf[idx++] = lo_bg; }
            }
        }
        for (int sy = 0; sy < scale; sy++) {
            spi_write_blocking(LCD_SPI, row_buf, idx);
        }
    }
    cs_high();
}

void lcd_draw_text(int x, int y, const char* s, int scale, uint16_t fg, uint16_t bg) {
    int step = 6 * scale;
    while (*s) {
        if (x + step > LCD_W) break;
        lcd_draw_char(x, y, *s, scale, fg, bg);
        x += step;
        s++;
    }
}

//-----------------------------------------------------------------------------
// ST7789 init
//-----------------------------------------------------------------------------
static void lcd_reset(void) {
    gpio_put(LCD_PIN_RST, 0);
    sleep_ms(20);
    gpio_put(LCD_PIN_RST, 1);
    sleep_ms(120);
}

void lcd_init(void) {
    gpio_init(LCD_PIN_CS);  gpio_set_dir(LCD_PIN_CS,  GPIO_OUT); gpio_put(LCD_PIN_CS,  1);
    gpio_init(LCD_PIN_DC);  gpio_set_dir(LCD_PIN_DC,  GPIO_OUT); gpio_put(LCD_PIN_DC,  0);
    gpio_init(LCD_PIN_RST); gpio_set_dir(LCD_PIN_RST, GPIO_OUT); gpio_put(LCD_PIN_RST, 1);
    gpio_init(LCD_PIN_BL);  gpio_set_dir(LCD_PIN_BL,  GPIO_OUT); gpio_put(LCD_PIN_BL,  1);

    spi_init(LCD_SPI, 40 * 1000 * 1000);
    gpio_set_function(LCD_PIN_SCK,  GPIO_FUNC_SPI);
    gpio_set_function(LCD_PIN_MOSI, GPIO_FUNC_SPI);

    lcd_reset();

    lcd_cmd(0x36); lcd_data_byte(0x60);  // MADCTL: landscape (90° CW), RGB
    lcd_cmd(0x3A); lcd_data_byte(0x05);  // COLMOD: 16-bit/pixel

    // Porch / power / gamma (Waveshare reference for 1.3" ST7789)
    lcd_cmd(0xB2); { uint8_t b[] = {0x0C,0x0C,0x00,0x33,0x33}; lcd_data(b, 5); }
    lcd_cmd(0xB7); lcd_data_byte(0x35);
    lcd_cmd(0xBB); lcd_data_byte(0x19);
    lcd_cmd(0xC0); lcd_data_byte(0x2C);
    lcd_cmd(0xC2); lcd_data_byte(0x01);
    lcd_cmd(0xC3); lcd_data_byte(0x12);
    lcd_cmd(0xC4); lcd_data_byte(0x20);
    lcd_cmd(0xC6); lcd_data_byte(0x0F);
    lcd_cmd(0xD0); { uint8_t b[] = {0xA4,0xA1}; lcd_data(b, 2); }
    lcd_cmd(0xE0); { uint8_t b[] = {0xD0,0x04,0x0D,0x11,0x13,0x2B,0x3F,0x54,0x4C,0x18,0x0D,0x0B,0x1F,0x23}; lcd_data(b, 14); }
    lcd_cmd(0xE1); { uint8_t b[] = {0xD0,0x04,0x0C,0x11,0x13,0x2C,0x3F,0x44,0x51,0x2F,0x1F,0x1F,0x20,0x23}; lcd_data(b, 14); }

    lcd_cmd(0x21);  // INVON (IPS panels need inversion)
    lcd_cmd(0x11);  // SLPOUT
    sleep_ms(120);
    lcd_cmd(0x29);  // DISPON

    lcd_fill(COLOR_BLACK);
}
