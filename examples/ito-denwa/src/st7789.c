// Waveshare Pico-LCD-1.3 pinout (ST7789, 240x240, SPI1)
#define LCD_SPI       spi1
#define LCD_PIN_SCK   10
#define LCD_PIN_MOSI  11
#define LCD_PIN_CS     9
#define LCD_PIN_DC     8
#define LCD_PIN_RST   12
#define LCD_PIN_BL    13

static inline void cs_low(void)  { gpio_put(LCD_PIN_CS, 0); }
static inline void cs_high(void) { gpio_put(LCD_PIN_CS, 1); }

static void lcd_cmd(uint8_t cmd) {
    cs_low();
    gpio_put(LCD_PIN_DC, 0);
    spi_write_blocking(LCD_SPI, &cmd, 1);
    cs_high();
}

static void lcd_data(const uint8_t* data, size_t len) {
    cs_low();
    gpio_put(LCD_PIN_DC, 1);
    spi_write_blocking(LCD_SPI, data, len);
    cs_high();
}

static void lcd_data_byte(uint8_t b) { lcd_data(&b, 1); }

static void lcd_set_window(int x0, int y0, int x1, int y1) {
    uint8_t buf[4];
    lcd_cmd(0x2A);  // CASET
    buf[0] = x0 >> 8; buf[1] = x0 & 0xFF;
    buf[2] = x1 >> 8; buf[3] = x1 & 0xFF;
    lcd_data(buf, 4);
    lcd_cmd(0x2B);  // RASET
    buf[0] = y0 >> 8; buf[1] = y0 & 0xFF;
    buf[2] = y1 >> 8; buf[3] = y1 & 0xFF;
    lcd_data(buf, 4);
    lcd_cmd(0x2C);  // RAMWR
}

void lcd_fill_rect(int x, int y, int w, int h, uint16_t color) {
    if (w <= 0 || h <= 0) return;
    if (x < 0 || y < 0 || x + w > LCD_W || y + h > LCD_H) return;
    lcd_set_window(x, y, x + w - 1, y + h - 1);

    uint8_t hi = color >> 8, lo = color & 0xFF;
    uint8_t buf[64];
    for (int i = 0; i < 64; i += 2) { buf[i] = hi; buf[i+1] = lo; }
    int total = w * h * 2;

    cs_low();
    gpio_put(LCD_PIN_DC, 1);
    while (total > 0) {
        int chunk = (total > 64) ? 64 : total;
        spi_write_blocking(LCD_SPI, buf, chunk);
        total -= chunk;
    }
    cs_high();
}

void lcd_fill(uint16_t color) { lcd_fill_rect(0, 0, LCD_W, LCD_H, color); }

